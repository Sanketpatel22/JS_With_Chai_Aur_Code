//assingment operator

// Assignment operator is used to assign values to variables

let a = 10; // assigns the value 10 to variable a
console.log(a); // Output: 10

a+= 5; // adds 5 to the current value of a and assigns it back to a
console.log(a); // Output: 15

a-= 3; // subtracts 3 from the current value of a and assigns it back to a
console.log(a); // Output: 12

a*= 2; // multiplies the current value of a by 2 and assigns it back to a
console.log(a); // Output: 24

