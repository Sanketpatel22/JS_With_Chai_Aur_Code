//arithmetic operator in javascript
//operator +, -, *, /, %, **, ++, --

let a = 10;
let b = 20;
console.log(a + b); //30
console.log(a - b); //-10
console.log(a * b); //200
console.log(a / b); //0.5
console.log(a % b); //10
console.log(a ** b); //10000000000000000000000 10 raised to 20
console.log(a++); //10
console.log(a); //11
console.log(++a); //12
console.log(a--); //12
console.log(a); //11
console.log(--a); //10
console.log(a); //10

